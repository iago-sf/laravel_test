     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(ImageForm $request)
    {
        $request->file('image')->store('public');
        
        $user = Auth::user();
        $user->image = asset('storage/'.$request->file('image')->hashName());
        $user->save();
        
        return back();
    }

